#include <stdio.h>

int main(){
    int a = 10;
    int b = 8;

    printf("sum of %d and %d = %d\n" , a , b, a+b);
    printf("difference of %d and %d = %d\n" , a , b, a-b);
    printf("multiplication of %d and %d = %d\n" , a , b, a*b);
    printf("division of %d and %d = %d\n" , a , b, a/b);
    
    return 0;
}